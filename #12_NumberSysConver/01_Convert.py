bin(25) # Convert decimal to binary 
print(bin(25)) # Bin function will return the binary value of the decimal number

# Output: 0b11001

# Binary to Decimal

print(0b11001) # 25

# Decimal to Octal

oct(25) # Convert decimal to octal
print(oct(25)) # 0o31

# Decimal to Hexadecimal

hex(25) # Convert decimal to hexadecimal
print(hex(25)) # 0x19

print(hex(10)) # 0xa